 		ori    %r1, %r1, 0xf300
 		li     %r2, 0x200
 		oris   %r2, %r2, 0x8000
 		sldi   %r2, %r2, 32		# 0x20
 		MAKEBRANCH 0x40c # Branch back to original function + 32 (0x20)
